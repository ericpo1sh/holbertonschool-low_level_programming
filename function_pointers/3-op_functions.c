#include "3-calc.h"
#include <stdio.h>
/**
 * op_add - adds two ints
 * @a : first int
 * @b : second int
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts two its
 * @a : first int
 * @b : second int
 * Return: sum
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies two ints
 * @a : first int
 * @b : second int
 * Return: sum
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides two ints
 * @a : first int
 * @b : second int
 * Return: sum
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - finds modulo of two ints
 * @a : first int
 * @b : second int
 * Return: sum
 */
int op_mod(int a, int b)
{
	return (a % b);
}
